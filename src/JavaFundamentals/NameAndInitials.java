package JavaFundamentals;

public class NameAndInitials {
    public static void main (String [] args){
        String firstName = "Tim";

        String middleName = "Scott";

        String lastName = "Jackson";

        char firstInitial = 'T';

        char middleInitial = 'S';

        char lastInitial = 'J';

        System.out.print("My name is " + firstName + " " + middleName + " " + lastName + ", my initials would be " + firstInitial + middleInitial
                + lastInitial + ".");
    }
}
